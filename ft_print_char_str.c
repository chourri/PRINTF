#include "ft_printf.h"

int	ft_print_char(int c)
{
	return (write (1, &c, 1));
}

int	ft_print_str(char *str)
{
	int	count;

	count = 0;
	if (str == NULL)
		return (write(1, "(null)", 6));
	while (*str)
	{
		ft_print_char(*str);
		str++;
		count++;
	}
	return (count);
}
